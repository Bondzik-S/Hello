// Вводим значения A и B
var A = parseInt(prompt("Введите значение A:"));
var B = parseInt(prompt("Введите значение B:"));

// Проверяем, что A больше B
if (A > B) {
    var sum = 0;
    
    // Вычисляем сумму чисел в промежутке от A до B
    for (var i = A; i >= B; i--) {
        sum += i;
    }
    
    // Выводим сумму
    console.log("Сумма чисел от " + B + " до " + A + " равна " + sum);

    // Выводим все нечётные значения
    console.log("Нечётные числа в промежутке от " + B + " до " + A + ":");
    for (var i = A; i >= B; i--) {
        if (i % 2 !== 0) {
            console.log(i);
        }
    }
} else {
    console.log("Значение A должно быть больше B");
}
